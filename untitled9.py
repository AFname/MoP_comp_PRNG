# -*- coding: utf-8 -*-
import csv
import matplotlib.pyplot as plt

# ══════════════════════════════════════════════════════════════════════════════
#  Загрузка CSV
# ══════════════════════════════════════════════════════════════════════════════
def load_statistics(filename):
    data = {}  # data[generator] = {'mean': [], 'stddev': [], 'cv': [], 'p_value': [], 'uniform': []}
    with open(filename, newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            g = row['generator']
            if g not in data:
                data[g] = {'sample': [], 'mean': [], 'stddev': [], 'cv': [], 'p_value': [], 'uniform': []}
            data[g]['sample'].append(int(row['sample']))
            data[g]['mean'].append(float(row['mean']))
            data[g]['stddev'].append(float(row['stddev']))
            data[g]['cv'].append(float(row['cv']))
            data[g]['p_value'].append(float(row['p_value']))
            data[g]['uniform'].append(int(row['uniform']))
    return data

def load_nist(filename):
    data = {}  # data[generator] = {'test_name': [], 'p_value': [], 'passed': []}
    with open(filename, newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            g = row['generator']
            if g not in data:
                data[g] = {'test_name': [], 'p_value': [], 'passed': []}
            data[g]['test_name'].append(row['test_name'])
            data[g]['p_value'].append(float(row['p_value']))
            data[g]['passed'].append(int(row['passed']))
    return data

def load_timing(filename):
    sizes = []
    cs    = []
    acc   = []
    qx    = []
    std   = []
    with open(filename, newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            sizes.append(int(row['size']))
            cs.append(float(row['CrossState']))
            acc.append(float(row['XorShiftACC']))
            qx.append(float(row['QuadXOR']))
            std.append(float(row['StdRand']))
    return sizes, cs, acc, qx, std

# ══════════════════════════════════════════════════════════════════════════════
#  График 1: Среднее по выборкам для каждого генератора
# ══════════════════════════════════════════════════════════════════════════════
def plot_mean(data):
    fig, ax = plt.subplots(figsize=(10, 6))
    for g, d in data.items():
        ax.plot(d['sample'], d['mean'], marker='o', label=g)
    ax.set_title('Среднее значение по выборкам')
    ax.set_xlabel('Номер выборки')
    ax.set_ylabel('Среднее')
    ax.legend()
    ax.grid(True)
    plt.tight_layout()
    plt.savefig('plot_mean.png', dpi=150)
    plt.close()
    print('Сохранено: plot_mean.png')

