# ══════════════════════════════════════════════════════════════════════════════
#  main
# ══════════════════════════════════════════════════════════════════════════════
def main():
    stats = load_statistics('statistics.csv')
    nist  = load_nist('nist_tests.csv')
    sizes, cs, acc, qx, std = load_timing('timing.csv')

    plot_mean(stats)
    plot_stddev(stats)
    plot_cv(stats)
    plot_chi_pvalue(stats)
    plot_nist(nist)
    plot_timing_all(sizes, cs, acc, qx, std)
