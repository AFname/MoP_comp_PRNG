#  График 4: p-value хи-квадрат по выборкам + граница 0.05
# ══════════════════════════════════════════════════════════════════════════════
def plot_chi_pvalue(data):
    fig, ax = plt.subplots(figsize=(10, 6))
    for g, d in data.items():
        ax.plot(d['sample'], d['p_value'], marker='o', label=g)
    ax.axhline(y=0.05, color='red', linestyle='--', label='p = 0.05')
    ax.set_title('p-value критерия хи-квадрат по выборкам')
    ax.set_xlabel('Номер выборки')
    ax.set_ylabel('p-value')
    ax.legend()
    ax.grid(True)
    plt.tight_layout()
    plt.savefig('plot_chi_pvalue.png', dpi=150)
    plt.close()
    print('Сохранено: plot_chi_pvalue.png')

# ══════════════════════════════════════════════════════════════════════════════
#  График 5: p-value NIST тестов (столбчатый, по генераторам)
# ══════════════════════════════════════════════════════════════════════════════
def plot_nist(data):
    generators = list(data.keys())
    tests      = data[generators[0]]['test_name']
    n_tests    = len(tests)
    n_gen      = len(generators)

    x     = list(range(n_tests))
    width = 0.25

    fig, ax = plt.subplots(figsize=(12, 6))
    for i, g in enumerate(generators):
        offsets = [xi + i * width for xi in x]
        pvals   = data[g]['p_value']
        ax.bar(offsets, pvals, width=width, label=g)

    ax.axhline(y=0.01, color='red', linestyle='--', label='p = 0.01 (NIST)')
    ax.set_title('p-value NIST тестов по генераторам')
    ax.set_xlabel('Тест')
    ax.set_ylabel('p-value')
    ax.set_xticks([xi + width * (n_gen - 1) / 2 for xi in x])
    ax.set_xticklabels(tests, rotation=15, ha='right')
    ax.legend()
    ax.grid(True, axis='y')
    plt.tight_layout()
    plt.savefig('plot_nist.png', dpi=150)
    plt.close()
    print('Сохранено: plot_nist.png')

# ══════════════════════════════════════════════════════════════════════════════
